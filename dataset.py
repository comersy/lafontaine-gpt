"""
Dataset for lafontaine-gpt.

Loads pre-encoded binary files (pretrain_ids.bin / finetune_ids.bin)
generated by tokenizer_train.exe encode.

If the .bin file does not exist, it is generated automatically
by calling tokenizer_train.exe encode.

Pretrain uses a continuous stream of tokens (TextDataset), since the
corpus has no meaningful document boundaries for our purposes.

Finetune uses per-fable sequences (FableDataset): each fable is its own
sequence, padded to block_size, so the model never sees two fables
stitched together in one window.

Usage:
    train_loader, val_loader = build_loaders(mode="pretrain", tokenizer=tokenizer)
    train_loader, val_loader = build_loaders(mode="finetune", tokenizer=tokenizer)
"""

import os
import subprocess
import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader

from tokenizer import BPETokenizer

# ── Hyperparameters ───────────────────────────────────────────────────────────

BLOCK_SIZE  = 512
BATCH_SIZE  = 32
TRAIN_SPLIT = 0.9

BIN_FILES = {
    "pretrain" : "pretrain_ids.bin",
    "finetune" : "finetune_ids.bin",
}
TOKENIZER_EXE = ".\\bpe_tokenizer\\target\\release\\tokenizer_train.exe"

PAD_ID = 0  # <pad>
BOS_ID = 2  # <bos>
EOS_ID = 3  # <eos>


# ── Auto-encode ───────────────────────────────────────────────────────────────

def ensure_encoded(mode: str) -> str:
    """
    Returns path to the .bin file for the given mode.
    If it does not exist, calls tokenizer_train.exe encode to generate it.
    """
    bin_path = BIN_FILES[mode]
    if not os.path.exists(bin_path):
        print(f"\n{bin_path} not found ===> encoding with Rust tokenizer...")
        subprocess.run(
            [TOKENIZER_EXE, "encode", "--mode", mode, "--output", bin_path],
            check=True
        )
        print()
    else:
        size_mb = os.path.getsize(bin_path) / 1_000_000
        print(f"Found {bin_path} ({size_mb:.0f} MB)")
    return bin_path


# ── Pretrain dataset: continuous stream ───────────────────────────────────────

class TextDataset(Dataset):
    """
    Loads token ids from a binary file (uint16, little-endian) as one
    continuous stream. Used for pretrain, where document boundaries
    don't matter for learning general French.
    """

    def __init__(
        self,
        bin_path    : str,
        block_size  : int   = BLOCK_SIZE,
        split       : str   = "train",
        train_split : float = TRAIN_SPLIT,
    ):
        assert split in ("train", "val")
        self.block_size = block_size

        print(f"Loading {bin_path}...")
        ids = np.fromfile(bin_path, dtype=np.uint16).astype(np.int64)
        print(f"  {len(ids):,} tokens total")

        split_idx = int(len(ids) * train_split)
        ids = ids[:split_idx] if split == "train" else ids[split_idx:]

        self.ids = torch.from_numpy(ids)
        print(f"  {split} ===> {len(self.ids):,} tokens, {len(self):,} sequences")

    def __len__(self) -> int:
        return (len(self.ids) - 1) // self.block_size

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, torch.Tensor]:
        start = idx * self.block_size
        x = self.ids[start     : start + self.block_size]
        y = self.ids[start + 1 : start + self.block_size + 1]
        return x, y


# ── Finetune dataset: per-fable sequences, no stitching ──────────────────────

class FableDataset(Dataset):
    """
    Splits the token stream into individual fables using BOS_ID/EOS_ID
    markers. Each fable becomes its own training sequence, padded to
    block_size. This guarantees the model never sees the end of one
    fable followed by the start of another in the same window.

    Fables longer than block_size are truncated (rare for La Fontaine).
    Loss on padding tokens is ignored via ignore_index in the model.
    """

    def __init__(
        self,
        bin_path    : str,
        block_size  : int   = BLOCK_SIZE,
        split       : str   = "train",
        train_split : float = TRAIN_SPLIT,
    ):
        assert split in ("train", "val")
        self.block_size = block_size

        print(f"Loading {bin_path}...")
        ids = np.fromfile(bin_path, dtype=np.uint16).astype(np.int64)
        print(f"  {len(ids):,} tokens total")

        # Split into individual fables at BOS...EOS boundaries
        fables = []
        cur = []
        for tok in ids:
            tok = int(tok)
            if tok == BOS_ID:
                cur = []
            elif tok == EOS_ID:
                if cur:
                    fables.append(cur)
                cur = []
            else:
                cur.append(tok)
        if cur:
            fables.append(cur)

        print(f"  {len(fables):,} fables found")

        split_idx = int(len(fables) * train_split)
        fables = fables[:split_idx] if split == "train" else fables[split_idx:]

        self.fables = fables
        print(f"  {split} ===> {len(self.fables):,} fables")

    def __len__(self) -> int:
        return len(self.fables)

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, torch.Tensor]:
        fable = self.fables[idx][: self.block_size + 1]

        # Pad with PAD_ID if shorter than block_size + 1
        if len(fable) < self.block_size + 1:
            fable = fable + [PAD_ID] * (self.block_size + 1 - len(fable))

        ids = torch.tensor(fable, dtype=torch.long)
        x = ids[:-1]
        y = ids[1:]
        return x, y


# ── Builder ───────────────────────────────────────────────────────────────────

def build_loaders(
    mode        : str,
    tokenizer   : BPETokenizer,
    block_size  : int   = BLOCK_SIZE,
    batch_size  : int   = BATCH_SIZE,
    train_split : float = TRAIN_SPLIT,
) -> tuple[DataLoader, DataLoader]:
    assert mode in ("pretrain", "finetune")
    print(f"\nBuilding {mode} loaders...")

    bin_path = ensure_encoded(mode)

    dataset_cls = TextDataset if mode == "pretrain" else FableDataset

    train_dataset = dataset_cls(bin_path, block_size, split="train", train_split=train_split)
    val_dataset   = dataset_cls(bin_path, block_size, split="val",   train_split=train_split)

    train_loader = DataLoader(train_dataset, batch_size=batch_size, shuffle=True,  drop_last=True)
    val_loader   = DataLoader(val_dataset,   batch_size=batch_size, shuffle=False, drop_last=True)

    return train_loader, val_loader


